package doctype;

public abstract class AbstractFileType {
    private String data;
    private String name;

    public AbstractFileType(String name) {
        this.name = name;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
